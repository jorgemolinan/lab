# gsub, reemplaza un caracter por otro
keyword = "chocolate"
keyword = keyword.gsub("o", "x")
puts keyword

# delete, borra un caracter
state = "Tarapaca"
state = state.delete("a")
puts state

# strip, borra espacios en blanco iniciales y finales
key = "  secret word  "
key = key.strip
puts key

